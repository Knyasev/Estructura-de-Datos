    private void cargarTabla() {
        modelo.setDatos(pd.listar());
        tblTabla.setModel(modelo);
        tblTabla.updateUI();
    }

    private void limpiar() {
        txtAnio.setValue(Calendar.getInstance().get(Calendar.YEAR));
        txtCliente.setText("");
        txtDireccion.setText("");
        txtCedula.setText("");
        cargarTabla();
        cargarCombo();
        fila = -1;
        pd.setPersona(null);

    }

    private void cargarCombo() {
        try {
            Utilidades.cargarFactura(cbxFactura, fd);
        } catch (Exception e) {
        }
    }

    private void guardar() {
        if (txtAnio.getValue().toString().isEmpty() || txtCliente.getText().trim().isEmpty()
                || txtDireccion.getText().trim().isEmpty() || txtCedula.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese todo los campos", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                pd.getPersona().setCedula(txtCedula.getText());
                pd.getPersona().setFecha_Nacimiento(Integer.valueOf(txtAnio.getValue().toString()));
                pd.getPersona().setCliente(txtCliente.getText());
                pd.getPersona().setDireccion(txtDireccion.getText());
                pd.getPersona().setId_Pais(fd.buscarPorNombre(cbxFactura.getSelectedItem().toString()).getId());
                if (pd.getPersona().getId() != null) {
                    pd.modificar(fila);
                    limpiar();
                } else {
                    pd.guardar();
                    limpiar();
                }
                JOptionPane.showMessageDialog(null, "Se ha guardado de forma correcta", "OK", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }

