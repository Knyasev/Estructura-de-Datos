    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        fila = tblTabla.getSelectedRow();
        if (fila >= 0) {
            try {
                Integer id = modelo.getDatos().get(fila).getId();
                pd.setPersona(pd.obtener(id));
                txtAnio.setValue(pd.getPersona().getFecha_Nacimiento());
                txtCliente.setText(pd.getPersona().getCliente());
                txtCedula.setText(pd.getPersona().getCedula());
                txtDireccion.setText((pd.getPersona().getDireccion()));
                cbxFactura.setSelectedItem(fd.obtener(pd.getPersona().getId_Pais()).getNacionalidad().toUpperCase());
            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Escoja un registro de la tabla", "Error", JOptionPane.ERROR_MESSAGE);

        }    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String texto = txtBuscar.getText();
        String criterio = cbxAtributo.getSelectedItem().toString();
        if (CbxMetododeBusqueda.getSelectedIndex() == 0) {
            switch (criterio) {
                case "Cliente":
                    try {
                    modelo.setDatos(pd.buscarPorNombresOrdenado( texto));
                } catch (Exception e) {
                }
                break;
                case "Direccion":
                    try {
                    modelo.setDatos(pd.busquedaBinariaOrdenada(pd.listar(), texto));

                } catch (Exception e) {
                }
                break;
                case "Cedula":
                    try {
                    modelo.setDatos(pd.busquedaBinariaOCedula(pd.listar(),texto));

                } catch (Exception e) {

                }
                break;
                case "Nacionalidad" :
                    try {
                    modelo.setDatos(pd.buscarPorNacionalidadB(texto));
                } catch (Exception e) {
                } 

            }
            tblTabla.setModel(modelo);
            tblTabla.updateUI();
        } else if (CbxMetododeBusqueda.getSelectedIndex() == 1) {
            switch (cbxAtributo.getSelectedIndex()) {
                case 0:
                    try {
                    modelo.setDatos(pd.buscarPorNombresSyB(texto));
                } catch (Exception e) {
                }
                break;
                case 1:
                    try {
                    modelo.setDatos(pd.buscarPorDireccionSyB(texto));

                } catch (Exception e) {
                }
                break;
                case 2:
                    try {
                    modelo.setDatos(pd.buscarPorCedulaSyB( texto));
                } catch (Exception e) {

                }
                break;
                case 3 :
                    try {
                    modelo.setDatos(pd.buscarPorNacionalidadBYS(texto));
                } catch (Exception e) {
                }
                    
            }
            tblTabla.setModel(modelo);
            tblTabla.updateUI();

        }

    }//GEN-LAST:event_jButton4ActionPerformed

